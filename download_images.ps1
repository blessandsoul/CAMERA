$outputDir = "c:\Users\User\Desktop\GITHUB\camera\images"
$client = New-Object System.Net.WebClient
$client.Headers.Add("User-Agent", "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36")
$client.Headers.Add("Referer", "https://netis.ge/")

$success = 0
$fail = 0

foreach ($url in $urls) {
    $filename = [System.IO.Path]::GetFileName([System.Uri]::UnescapeDataString($url))
    # Sanitize filename
    $filename = $filename -replace '[^\w\-_\.]', '_'
    $filename = $filename -replace '__+', '_'
    $outPath = Join-Path $outputDir $filename

    try {
        $client.DownloadFile($url, $outPath)
        Write-Host "OK: $filename"
        $success++
    } catch {
        Write-Host "FAIL: $url - $_"
        $fail++
    }
    Start-Sleep -Milliseconds 100
}

Write-Host "`nDone: $success ok, $fail failed"
